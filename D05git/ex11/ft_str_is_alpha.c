int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while ((str[i] >= 'a' && str[i] <= 'z') || (str[i] >= 'A' && str[i] <= 'Z'))
	{
		if ((str[i] < 'a' && str[i] > 'Z') || str[i] > 'z' || str[i] < 'A')
			return (0);
		if (str[i + 1] == '\0')
			return (1);
		i++;
	}
	return (0);
}
